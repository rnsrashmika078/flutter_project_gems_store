class Gem {
  final String id;
  final String name;
  final String type;
  final double price;
  final String location;
  final String imagePath;
  final String owner;

  Gem({
    required this.id,
    required this.name,
    required this.type,
    required this.price,
    required this.location,
    required this.imagePath,
    required this.owner
  });
}
